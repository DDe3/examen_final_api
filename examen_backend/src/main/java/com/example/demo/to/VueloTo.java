package com.example.demo.to;

public class VueloTo {
	
	private String numeroVuelo;
	private String origen;
	private String destino;
	private String categoria;
	private String nombre;
	private Double valorAsiento;
	private Integer numeroAsientos;
	
	
	
	
	
	public Integer getNumeroAsientos() {
		return numeroAsientos;
	}
	public void setNumeroAsientos(Integer numeroAsientos) {
		this.numeroAsientos = numeroAsientos;
	}
	public String getNumeroVuelo() {
		return numeroVuelo;
	}
	public void setNumeroVuelo(String numeroVuelo) {
		this.numeroVuelo = numeroVuelo;
	}
	public String getOrigen() {
		return origen;
	}
	public void setOrigen(String origen) {
		this.origen = origen;
	}
	public String getDestino() {
		return destino;
	}
	public void setDestino(String destino) {
		this.destino = destino;
	}
	public String getCategoria() {
		return categoria;
	}
	public void setCategoria(String categoria) {
		this.categoria = categoria;
	}
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	public Double getValorAsiento() {
		return valorAsiento;
	}
	public void setValorAsiento(Double valorAsiento) {
		this.valorAsiento = valorAsiento;
	}
	
	
	

}
